public class Car {

    private String StartString;
    private String endString;
    private String rateCode;
    private String price;
    private String numDays;
    private String carCode;
    private String garanty;
    private String addionalString;
    private String participationLevel;

    public String getStartString() {
        return StartString;
    }

    public void setStartString(String startString) {
        StartString = startString;
    }

    public String getEndString() {
        return endString;
    }

    public void setEndString(String endString) {
        this.endString = endString;
    }

    public String getRateCode() {
        return rateCode;
    }

    public void setRateCode(String rateCode) {
        this.rateCode = rateCode;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getNumDays() {
        return numDays;
    }

    public void setNumDays(String numDays) {
        this.numDays = numDays;
    }

    public String getCarCode() {
        return carCode;
    }

    public void setCarCode(String carCode) {
        this.carCode = carCode;
    }

    public String getGaranty() {
        return garanty;
    }

    public void setGaranty(String garanty) {
        this.garanty = garanty;
    }

    public String getAddionalString() {
        return addionalString;
    }

    public void setAddionalString(String addionalString) {
        this.addionalString = addionalString;
    }

    public String getParticipationLevel() {
        return participationLevel;
    }

    public void setParticipationLevel(String participationLevel) {
        this.participationLevel = participationLevel;
    }
}
